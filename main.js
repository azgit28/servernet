  function myFunction(index) {
  
      const z = data[index]
      const x = document.getElementById("form");
      const y = document.getElementById("body");
      var popup = document.getElementById("popup");
      var left = document.getElementById("left");
      const head = document.getElementById('head')
      const imageElement = document.getElementById('myImage');
      
      if (x.style.display === "block") {
        x.style.display = "none";
        y.style.filter = "blur(0px)";
        
      } else {
        x.style.display = "block";
        y.style.filter = "blur(2px)";
        x.style.filter = "blur(0px)";
        head.textContent = z.txt
        imageElement.src = z.src
  
        setTimeout(function () {
          left.style.display = "none";
          popup.style.display = "block";
        }, 3000);
      }
      const h = document.getElementById("formed");
      const g = document.getElementById("formsed");
      const l = document.getElementById("text");
      h.addEventListener("click", e => {
        if (g.style.display === "none") {
          g.style.display = "block";
          popup.style.display = "none";
          l.value= z.txt
  
          
        } else {
          g.style.display = "none";
        }
        
      })
  }
  
  
  
  
  
  
  
  
  
  
